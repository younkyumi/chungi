import Anthropic from "@anthropic-ai/sdk";
import { NextRequest, NextResponse } from "next/server";

const anthropic = new Anthropic({
  apiKey: process.env.ANTHROPIC_API_KEY,
});

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { serviceType, prompt, userData } = body;

    const userPrompt = buildUserPrompt(serviceType, prompt, userData);

    const message = await anthropic.messages.create({
      model: "claude-haiku-4-5-20251001",
      max_tokens: 2048,
      messages: [
        {
          role: "user",
          content: userPrompt,
        },
      ],
      system: getSystemPrompt(serviceType),
    });

    const text =
      message.content[0].type === "text" ? message.content[0].text : "";

    return NextResponse.json({ result: text });
  } catch (error: unknown) {
    const errorMessage =
      error instanceof Error ? error.message : "분석 중 오류가 발생했습니다.";
    return NextResponse.json({ error: errorMessage }, { status: 500 });
  }
}

function buildUserPrompt(
  serviceType: string,
  prompt: string,
  userData?: {
    name?: string;
    birthYear?: string;
    birthMonth?: string;
    birthDay?: string;
    birthTime?: string;
    zodiacSign?: string;
    question?: string;
    dreamDescription?: string;
    partnerName?: string;
    partnerBirthYear?: string;
    partnerBirthMonth?: string;
    partnerBirthDay?: string;
    fullName?: string;
  }
): string {
  if (!userData) return prompt;

  switch (serviceType) {
    case "사주풀이":
      return `이름: ${userData.name || "미제공"}
생년월일: ${userData.birthYear || ""}년 ${userData.birthMonth || ""}월 ${userData.birthDay || ""}일
태어난 시간: ${userData.birthTime || "미제공"}

위 정보를 바탕으로 사주팔자를 상세히 분석해주세요.`;

    case "오늘운세":
      return `띠/별자리: ${userData.zodiacSign || "미제공"}
생년월일: ${userData.birthYear || ""}년 ${userData.birthMonth || ""}월 ${userData.birthDay || ""}일

오늘의 운세를 자세히 알려주세요.`;

    case "타로":
      return `질문: ${userData.question || prompt}

이 질문에 대해 타로 카드 리딩을 해주세요.`;

    case "꿈해몽":
      return `꿈 내용: ${userData.dreamDescription || prompt}

이 꿈을 해석해주세요.`;

    case "연애운":
      return `본인 이름: ${userData.name || "미제공"}
본인 생년월일: ${userData.birthYear || ""}년 ${userData.birthMonth || ""}월 ${userData.birthDay || ""}일
상대방 이름: ${userData.partnerName || "미제공"}
상대방 생년월일: ${userData.partnerBirthYear || ""}년 ${userData.partnerBirthMonth || ""}월 ${userData.partnerBirthDay || ""}일

두 사람의 연애 궁합을 분석해주세요.`;

    case "성명학":
      return `이름: ${userData.fullName || userData.name || prompt}

이 이름을 성명학적으로 분석해주세요.`;

    case "기질도":
      return `이름: ${userData.name || "미제공"}
생년월일: ${userData.birthYear || ""}년 ${userData.birthMonth || ""}월 ${userData.birthDay || ""}일

오행 기질도를 분석해주세요.`;

    case "종합분석":
      return `이름: ${userData.name || "미제공"}
생년월일: ${userData.birthYear || ""}년 ${userData.birthMonth || ""}월 ${userData.birthDay || ""}일
태어난 시간: ${userData.birthTime || "미제공"}

위 정보를 바탕으로 종합 운세 분석을 해주세요.`;

    default:
      return prompt;
  }
}

