// 사주 텍스트에서 첫 등장 용어를 자동으로 풀이 추가
// - 이미 풀이된 경우 ('정관격(...)' 또는 '정관격 — ...') 스킵
// - 길이 긴 용어 우선 매칭 (정관격 > 정관)
export function enrichSajuText(text:string):string{
  if(!text||typeof text!=="string")return text;
  let out=text;
  const seen=new Set<string>();
  const keys=Object.keys(SAJU_GLOSS).sort((a,b)=>b.length-a.length);
  for(const k of keys){
    if(seen.has(k))continue;
    const idx=out.indexOf(k);
    if(idx<0)continue;
    const after=out.slice(idx+k.length,idx+k.length+3);
    if(after.startsWith("(")||after.startsWith("(")||after.match(/^\s*—/)||after.match(/^\s*\(/))continue;
    out=out.slice(0,idx)+SAJU_GLOSS[k]+out.slice(idx+k.length);
    seen.add(k);
  }
  return out;
}

