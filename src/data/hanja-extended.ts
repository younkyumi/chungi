// 검색 헬퍼 — q는 한자/음/뜻 중 하나
export function searchHanjaExt(q:string,strokeFilter?:number):HanjaItem[]{
  const query=q.trim().toLowerCase();
  if(!query)return [];
  return HANJA_EXT.filter(h=>{
    if(strokeFilter&&h.s!==strokeFilter)return false;
    return h.y.includes(query)||h.m.toLowerCase().includes(query)||h.c.includes(query);
  });
}
